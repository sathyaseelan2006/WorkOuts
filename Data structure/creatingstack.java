import java.util.Stack;

public class creatingstack {
    public static void main(String[] args){ 
    Stack<Object> s1 = new Stack<>();
        s1.push(10);
        s1.push(20);
        s1.push(30);
        s1.push(40);
        s1.push(50);
        System.out.println("my stack:"+s1);
        System.out.println("pop element:"+s1.pop());
        System.out.println("my stack:"+s1);
        System.out.println("peek element:"+s1.peek());
        System.out.println("my stack:"+s1);
        System.out.println("search element:"+s1.search(30));
        System.out.println("search element:"+s1.search(100));
    
}
}













































































































































































































































































































